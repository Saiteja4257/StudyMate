import { Request, Response } from "express";
import { StudySpace } from "../models/StudySpace";
import {
  generateCourseCurriculum,
  generateModuleContent,
  askStudySpaceTutor,
} from "../services/studySpaceAiService";

export const generateStudySpace = async (req: Request, res: Response) => {
  try {
    const { topic, goal } = req.body;
    const userId = (req as any).user?.id;

    if (!topic || !goal) {
      return res.status(400).json({ message: "Topic and goal are required" });
    }

    const curriculumStr = await generateCourseCurriculum(topic, goal);
    
    if (!curriculumStr) {
      return res.status(500).json({ message: "Failed to generate curriculum from AI" });
    }

    // Try to parse the curriculum json
    let curriculumData;
    try {
      const match = curriculumStr.match(/\{[\s\S]*\}/);
      const jsonStr = match ? match[0] : curriculumStr;
      curriculumData = JSON.parse(jsonStr);
    } catch (e) {
      console.error("Failed to parse AI response:", curriculumStr);
      return res.status(500).json({ message: "Invalid curriculum generated by AI" });
    }

    const newStudySpace = new StudySpace({
      userId,
      topic,
      goal,
      title: curriculumData.title || `${topic} Course`,
      description: curriculumData.description || `A comprehensive guide on ${topic}.`,
      modules: curriculumData.modules.map((m: any, index: number) => ({
        title: m.title,
        order: m.order || index + 1,
        summary: m.summary || "",
        contentGenerated: false,
      })),
    });

    await newStudySpace.save();

    res.status(201).json(newStudySpace);
  } catch (error) {
    console.error("Error generating study space:", error);
    res.status(500).json({ message: "Internal server error" });
  }
};

