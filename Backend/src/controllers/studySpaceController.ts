export const getModuleContent = async (req: Request, res: Response) => {
  try {
    const { id } = req.params;
    const userId = (req as any).user?.id;
    
    const space = await StudySpace.findOne({ _id: id, userId });
    if (!space) {
      return res.status(404).json({ message: "Study space not found" });
    }
    
    const moduleId = req.params.moduleId as string;
    const module = space.modules.id(moduleId);
    if (!module) {
      return res.status(404).json({ message: "Module not found" });
    }
    
    // If already generated, return it
    if (module.contentGenerated) {
      return res.status(200).json(module);
    }
    
    // Generate content lazily
    const contentStr = await generateModuleContent(space.title, space.topic, module.title);
    
    if (!contentStr) {
      return res.status(500).json({ message: "Failed to generate module content" });
    }
    
    let contentData;
    try {
      const match = contentStr.match(/\{[\s\S]*\}/);
      const jsonStr = match ? match[0] : contentStr;
      contentData = JSON.parse(jsonStr);
    } catch (e) {
      console.error("Failed to parse module content:", contentStr);
      return res.status(500).json({ message: "Invalid module content generated by AI" });
    }
    
    // Update module
    module.summary = contentData.summary || module.summary;
    module.detailedNotes = contentData.detailedNotes || "";
    module.keyConcepts = contentData.keyConcepts || [];
    module.flashcards = contentData.flashcards || [];
    module.quizzes = contentData.quizzes || [];
    module.visualTopics = contentData.visualTopics || null;
    module.contentGenerated = true;
    
    await space.save();
    
    res.status(200).json(module);
  } catch (error) {
    console.error("Error generating module content:", error);
    res.status(500).json({ message: "Internal server error" });
  }
};

export const updateModuleProgress = async (req: Request, res: Response) => {
  try {
    const { id } = req.params;
    const { completed } = req.body;
    const userId = (req as any).user?.id;
    
    const space = await StudySpace.findOne({ _id: id, userId });
    if (!space) {
      return res.status(404).json({ message: "Study space not found" });
    }
    
    const moduleId = req.params.moduleId as string;
    const module = space.modules.id(moduleId);
    if (!module) {
      return res.status(404).json({ message: "Module not found" });
    }
    
    module.completed = completed;
    
    // Recalculate progress
    const completedCount = space.modules.filter(m => m.completed).length;
    space.progress = Math.round((completedCount / space.modules.length) * 100);
    
    await space.save();
    
    res.status(200).json(space);
  } catch (error) {
    console.error("Error updating module progress:", error);
    res.status(500).json({ message: "Internal server error" });
  }
};

